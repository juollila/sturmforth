CR .( LOADING TEST SUITE: DCMP) CR

VARIABLE EXE
VARIABLE OK
VARIABLE FAIL
VARIABLE STEP

: ISUITE
0 EXE ! 0 OK ! 0 FAIL ! ;
: CHECK0
  DEPTH 0= IF 1 STEP +! THEN ;
: CHECK1
  DEPTH 1 = IF 1 STEP +! THEN ;
: CHECK2
  DEPTH 2 = IF 1 STEP +! THEN ;
: CHECK3
  DEPTH 3 = IF 1 STEP +! THEN ;
: CHECK4
  DEPTH 4 = IF 1 STEP +! THEN ;
: ASSERT
  = IF 1 STEP +! THEN ;
: DASSERT
  D= IF 1 STEP +! THEN ;
: PROK
  ." ok" CR ;
: PRNOK
  ." fail" CR ;
: RESULT
  = IF 1 OK +! PROK ELSE 1 FAIL +! PRNOK THEN ;
: ICASE
  1 EXE +! 0 STEP ! ;
: STAT
  CR ." eXECUTED " OK @ . ." / " EXE @ .
  ." CASES SUCCESSFULLY" CR ;

: TD0=
  ." TESTING D0= - "
  ICASE
  32000000. 0. CHECK4
  D0= 1 ASSERT
  D0= 0 ASSERT
  CHECK0
  STEP @ 4 RESULT
;

: TD=
  ." TESTING D= - "
  ICASE
  32000000. 0. CHECK4
  D= 0 ASSERT
  -32000000. -32000000. CHECK4
  D= 1 ASSERT
  CHECK0
  STEP @ 5 RESULT
;

: TD<
  ." TESTING D< - "
  ICASE
  32000000. 0. CHECK4
  D< 0 ASSERT
  -32000000. -32000000.
  D< 0 ASSERT
  -64000000. -32000000.
  D< 1 ASSERT
  CHECK0
  STEP @ 5 RESULT
;

: DCMP
  CR ISUITE
  TD0=
  TD=
  TD<
  STAT
;
